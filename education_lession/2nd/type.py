# - Строки (strings) - последовательность символов, заключенных в кавычки.
# - Числа (numbers) - целые числа, числа с плавающей точкой и комплексные числа.
# - Списки (lists) - упорядоченные коллекции элементов.
# - Кортежи (tuples) - неизменяемые упорядоченные коллекции элементов.
# - Словари (dictionaries) - неупорядоченные коллекции пар ключ-значение.