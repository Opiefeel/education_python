# - –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ü–∏—Ñ—Ä –Ω–µ –º–µ–Ω–µ–µ 1
# - –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–∞–ª–µ–Ω—å–∫–∏—Ö –±—É–∫–≤ –Ω–µ –º–µ–Ω–µ–µ 1
# - –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –±–æ–ª—å—à–∏—Ö –±—É–∫–≤ –Ω–µ –º–µ–Ω–µ–µ 1
