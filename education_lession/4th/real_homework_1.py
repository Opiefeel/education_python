# –í—Å–µ —á—Ç–æ –≤–∞–º –Ω—É–∂–Ω–æ, —ç—Ç–æ –∑–∞–∫–æ–Ω—á–∏—Ç–µ —ç—Ç–æ—Ç –∫–æ–¥. (–ó–∞–≥–ª—É—à–∫—É `pass` –∏–∑ —Ü–∏–∫–ª–∞ –º–æ–∂–Ω–æ —É–±—Ä–∞—Ç—å)

# –ö–æ–Ω—Å—Ç–∞–Ω—Ç—ã
THRESHOLD_DIGITS = 1
THRESHOLD_UPPER = 1
THRESHOLD_LOWER = 1
THRESHOLD_LEN = 8

# C—á–µ—Ç—á–∏–∫–∏ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
digit_counter = 0
upper_counter = 0
lower_counter = 0

input_password = str(input("–í–≤–µ–¥–∏—Ç–µ –ø–∞—Ä–æ–ª—å (–≤—Å–µ –ø—Ä–æ–±–µ–ª—ã –≤–≤–µ–¥—ë–Ω–Ω—ã–µ –≤ –ø–∞—Ä–æ–ª–µ –±—É–¥—É—Ç —É–¥–∞–ª–µ–Ω—ã): ")).replace(" ", "")
sum_symbols = len(input_password)

for i in input_password:
    if i.isdigit():
        digit_counter += 1
    if i.isupper():
        upper_counter += 1
    if i.islower():
        lower_counter += 1

if digit_counter < THRESHOLD_DIGITS:
    print(f'–í –ø–∞—Ä–æ–ª–µ –Ω—É–∂–Ω–∞ –∫–∞–∫ –º–∏–Ω–∏–º—É–º –æ–¥–Ω–∞ —Ü–∏—Ñ—Ä–∞')
elif digit_counter < THRESHOLD_UPPER:
    print(f'–í –ø–∞—Ä–æ–ª–µ –Ω—É–∂–Ω–∞ –∫–∞–∫ –º–∏–Ω–∏–º—É–º –æ–¥–Ω–∞ –º–∞–ª–µ–Ω—å–∫–∞—è –±—É–∫–≤–∞')
elif upper_counter < THRESHOLD_LOWER:
    print(f'–í –ø–∞—Ä–æ–ª–µ –Ω—É–∂–Ω–∞ –∫–∞–∫ –º–∏–Ω–∏–º—É–º –æ–¥–Ω–∞ –±–æ–ª—å—à–∞—è –±—É–∫–≤–∞')
elif sum_symbols <= THRESHOLD_LEN:
    print(f'–ü–∞—Ä–æ–ª—å –¥–æ–ª–∂–µ–Ω —Å–æ–¥–µ—Ä–∂–∞—Ç—å –∫–∞–∫ –º–∏–Ω–∏–º—É–º {THRESHOLD_LEN} —Å–∏–º–≤–æ–ª–æ–≤')
else:
    print(f'–ü–∞—Ä–æ–ª—å "{input_password}" –Ω–∞–¥–µ–∂–Ω—ã–π, —Å–æ–≤–µ—Ç—É—é –µ–≥–æ –∑–∞–ø–∏—Å–∞—Ç—å :)')
